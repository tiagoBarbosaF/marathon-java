package tiagobarbosa.marathonjava.introduction;

public class Class02PrimitiveTypes {
    public static void main(String[] args) {
        int age = 10;
        long bigNumber = 100000L;
        double salaryDouble = 7000D;
        float salaryFloat = 7500F;
        byte ageByte = 127;
        short ageShort = 10;
        boolean isTrue = true;
        boolean isFalse = false;
        char character = 'M';

        System.out.println("He's " + age + " years old.");
        System.out.println(bigNumber);
        System.out.println(salaryDouble);
        System.out.println(salaryFloat);
        System.out.println(ageByte);
        System.out.println(ageShort);
        System.out.println(isTrue);
        System.out.println(isFalse);
        System.out.println(character);
    }
}
